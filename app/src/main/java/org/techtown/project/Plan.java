package org.techtown.project;

public class Plan {
    String place;
    int year;
    int month;
    int sDate;
    int eDate;

    public String getPlace() {
        return place;
    }

    public void setPlace(String place) {
        this.place = place;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public int getMonth() {
        return month;
    }

    public void setMonth(int month) {
        this.month = month;
    }

    public int getsDate() {
        return sDate;
    }

    public void setsDate(int sDate) {
        this.sDate = sDate;
    }

    public int geteDate() {
        return eDate;
    }

    public void seteDate(int eDate) {
        this.eDate = eDate;
    }
}
